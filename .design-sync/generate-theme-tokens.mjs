// Generates a static CSS file exposing every MixJam theme's tokens as real,
// discoverable design tokens (`[data-theme-key="<key>"] { --token: value; }`),
// mirroring the runtime behavior of theme/themes.ts's applyTheme(). The repo's
// themes are applied at runtime via inline style.setProperty() calls, so
// without this file the 8 themes have no static CSS representation for the
// design tool's self-check to find. Source of truth: public/themes/*.json —
// this script only reshapes that data, never invents values. Re-run whenever
// a theme JSON changes: node .design-sync/generate-theme-tokens.mjs
import { readFileSync, readdirSync, writeFileSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..')
const THEMES_DIR = join(ROOT, 'public', 'themes')
const INDEX_CSS = join(ROOT, 'src', 'renderer', 'src', 'index.css')
// cfg.cssEntry is copied verbatim (no @import resolution) — concatenate here
// instead of shipping an @import the converter won't follow.
const OUT_PATH = join(ROOT, '.design-sync', 'design-sync-styles.css')

const files = readdirSync(THEMES_DIR).filter((f) => f.endsWith('.json')).sort()

function fontStack(family, fallback) {
  return `'${family}', ${fallback}`
}

function block(theme) {
  const lines = [`[data-theme-key="${theme.key}"] {`]
  for (const [token, value] of Object.entries(theme.colors)) lines.push(`  --${token}: ${value};`)
  for (const [token, value] of Object.entries(theme.depth)) lines.push(`  --${token}: ${value};`)
  lines.push(`  --radius: ${theme.radius};`)
  lines.push(`  --font-chrome: ${fontStack(theme.fonts.chrome, 'system-ui, sans-serif')};`)
  lines.push(`  --font-label: ${fontStack(theme.fonts.label, 'system-ui, sans-serif')};`)
  lines.push(`  --font-mono: ${fontStack(theme.fonts.mono, "'Consolas', monospace")};`)
  lines.push('}')
  return lines.join('\n')
}

const blocks = files.map((f) => block(JSON.parse(readFileSync(join(THEMES_DIR, f), 'utf8'))))

const header = `/* GENERATED by .design-sync/generate-theme-tokens.mjs — do not hand-edit.
   Concatenation of src/renderer/src/index.css (verbatim) plus static theme
   tokens derived from public/themes/*.json, mirroring theme/themes.ts's
   applyTheme(). cfg.cssEntry is copied verbatim by the converter (no @import
   resolution), so this file inlines everything the design-sync build needs
   rather than relying on @import. All ${files.length} MixJam themes
   (${files.map((f) => f.replace('.json', '')).join(', ')}) are included as
   [data-theme-key="<key>"] blocks — real, discoverable design tokens instead
   of only the default emerald bootstrap values index.css hardcodes. */\n\n`

const indexCss = readFileSync(INDEX_CSS, 'utf8')
const themeSection = `/* ---- generated theme tokens (${files.length} themes) ---- */\n\n${blocks.join('\n\n')}\n`

writeFileSync(OUT_PATH, header + indexCss + '\n' + themeSection)
console.error(`wrote ${OUT_PATH} (index.css + ${files.length} themes: ${files.map((f) => f.replace('.json', '')).join(', ')})`)
